    public void mostrarPessoa(Pessoa p){
        
        if (p instanceof PessoaFisica){
            PessoaFisica f = (PessoaFisica)p;
            txtFieldNome.setText(f.getNome());
            txtFieldCPF.setText(f.getCpf());
            txtFieldrg.setText(f.getRg());
            txtFieldTelefone1.setText(f.getTelefone());
            txtFieldEndereco.setText(f.getEndereco());
            txtFieldTelefone2.setText(f.getCelular());
            txtFieldEmail.setText(f.getEmail());
            
         
        }else if (p instanceof PessoaJuridica){
            PessoaJuridica j = (PessoaJuridica)p;
            txtFieldFantasia.setText(j.getNome());
            txtFieldEndereco1.setText(j.getEndereco());
            txtFieldEstadual.setText(j.getInscricaoestadual());
            txtFieldTelefone3.setText(j.getTelefone());
            txtFieldcnpj.setText(j.getCnpj());
            txtFieldEndereco1.setText(j.getEndereco());
            txtFieldEmail1.setText(j.getEmail());
        }
        desabilitarCampos();
        this.setVisible(true);    
    }
    
