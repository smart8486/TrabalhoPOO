    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed

            Pessoa p;
            if (this.jRadioButtonFisica.isSelected()){
                p = new PessoaFisica();
                PessoaFisica fisica = (PessoaFisica) p;
                fisica.setNome(txtFieldNome.getText());
                fisica.setCpf(txtFieldCPF.getText());
                fisica.setEndereco(txtFieldEndereco.getText());
                fisica.setTelefone(txtFieldTelefone1.getText());
                fisica.setCelular(txtFieldTelefone2.getText());
                fisica.setRg(txtFieldrg.getText());
                fisica.setEmail(txtFieldEmail.getText());
                
            } else{
                p = new PessoaJuridica();
                PessoaJuridica juridica = (PessoaJuridica) p;
                juridica.setNome(txtFieldFantasia.getText());
                juridica.setEndereco(txtFieldEndereco1.getText());
                juridica.setTelefone(txtFieldTelefone3.getText());
                juridica.setCnpj(txtFieldcnpj.getText());
                juridica.setInscricaoestadual(txtFieldEstadual.getText());
                juridica.setEmail(txtFieldEmail1.getText());
                
            }
            
            
            this.telaVisualizar.addPessoa(p);
            
         
            JOptionPane.showMessageDialog(this, "Cadastrado com sucesso","Sucesso",JOptionPane.INFORMATION_MESSAGE);
            this.limparCampos();
            
            //não consegui  unir duas arrays para que ambas assumam a mesma posição, conforme queria nos campos Nome/Empresa
            //Consegui fazê-lo mas adicionava mais outra array, fazendo com que repetisse uma em branco toda vez que fosse 
            //efetuado o cadastro
           
    
          
    }//GEN-LAST:event_btnCadastrarActionPerformed

